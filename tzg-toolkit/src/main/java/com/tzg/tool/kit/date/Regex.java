package com.tzg.tool.kit.date;

/**
 * 常见日期格式正则
 */
public class Regex {

    /**
     * 年份正则:YYYY
     */
    public static final String YEAR            = "[0-9]{3}[1-9]|[0-9]{2}[1-9][0-9]{1}|[0-9]{1}[1-9][0-9]{2}|[1-9][0-9]{3}";
    /**
     * DD/MM/YYYY
     */
    public static final String DD_MM_YYYY      = "^(?:(?:(?:0?[1-9]|1[0-9]|2[0-8])([-/.]?)(?:0?[1-9]|1[0-2])|(?:29|30)([-/.]?)(?:0?[13-9]|1[0-2])|31([-/.]?)(?:0?[13578]|1[02]))([-/.]?)(?!0000)[0-9]{4}|29([-/.]?)0?2([-/.]?)(?:[0-9]{2}(?:0[48]|[2468][048]|[13579][26])|(?:0[48]|[2468][048]|[13579][26])00))$";
    /***
     * 日期正则 支持： YYYY-MM-DD YYYY/MM/DD YYYY_MM_DD YYYY.MM.DD的形式
     * YYYYMMDD 暂不支持
     */
    public static final String YEAR_MONTH_DATE = "((^((1[8-9]\\d{2})|([2-9]\\d{3}))([-\\/\\._])(10|12|0?[13578])([-\\/\\._])(3[01]|[12][0-9]"
                                                 + "|0?[1-9])$)|(^((1[8-9]\\d{2})|([2-9]\\d{3}))([-\\/\\._])(11|0?[469])([-\\/\\._])(30|[12][0-9]"
                                                 + "|0?[1-9])$)|(^((1[8-9]\\d{2})|([2-9]\\d{3}))([-\\/\\._])(0?2)([-\\/\\._])(2[0-8]|1[0-9]|0?[1-"
                                                 + "9])$)|(^([2468][048]00)([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([3579][26]00)([-\\/\\._])(0?2)"
                                                 + "([-\\/\\._])(29)$)|(^([1][89][0][48])([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([2-9][0-9][0][48])"
                                                 + "([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([1][89][2468][048])([-\\/\\._])(0?2)([-\\/\\._])(29)$)|"
                                                 + "(^([2-9][0-9][2468][048])([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([1][89][13579][26])([-\\/\\._])"
                                                 + "(0?2)([-\\/\\._])(29)$)|(^([2-9][0-9][13579][26])([-\\/\\._])(0?2)([-\\/\\._])(29)$))";
    /**
     * 日期验证，验证的格式有："yyyyMM","yyyyMMdd","yyyyMMdd HH:mm:ss",
     *                            "yyyy-MM","yyyy-MM-dd","yyyy-MM-dd HH:mm:ss"
     *                            "yyyy.MM","yyyy.MM.dd","yyyy.MM.dd HH:mm:ss"
     *                            "yyyy/MM","yyyy/MM/dd","yyyy/MM/dd HH:mm:ss"
     *                            "yyyy_MM","yyyy_MM_dd","yyyy_MM_dd HH:mm:ss"
     */
    public static final String DATE_TIME       = "("
                                                 +
                                                 //第一种情况为月份是大月的有31天。
                                                 "(^\\d{3}[1-9]|\\d{2}[1-9]\\d{1}|\\d{1}[1-9]\\d{2}|[1-9]\\d{3}"
                                                 + //年
                                                 "([-/\\._]?)"
                                                 + //时间间隔符(-,/,.,_)
                                                 "(10|12|0?[13578])"
                                                 + //大月
                                                 "([-/\\._]?)"
                                                 + //时间间隔符(-,/,.,_)
                                                 "((3[01]|[12][0-9]|0?[1-9])?)"
                                                 + //日(31)要验证年月因此出现0/1次
                                                 "([\\s]?)"
                                                 + //空格
                                                 "((([0-1]?[0-9]|2[0-3]):([0-5]?[0-9]):([0-5]?[0-9]))?))$"
                                                 + //时分秒
                                                 "|"
                                                 + //或
                                                 //第二种情况为月份是小月的有30天，不包含2月。
                                                 "(^\\d{3}[1-9]|\\d{2}[1-9]\\d{1}|\\d{1}[1-9]\\d{2}|[1-9]\\d{3}"
                                                 + //年
                                                 "([-/\\._]?)"
                                                 + //时间间隔符(-,/,.,_)
                                                 "(11|0?[469])"
                                                 + //小月不含2月
                                                 "([-/\\._]?)"
                                                 + //时间间隔符(-,/,.,_)
                                                 "(30|[12][0-9]|0?[1-9])"
                                                 + //日(30)
                                                 "([\\s]?)"
                                                 + //空格
                                                 "((([0-1]?[0-9]|2[0-3]):([0-5]?[0-9]):([0-5]?[0-9]))?))$"
                                                 + //时分秒
                                                 "|"
                                                 + //或
                                                 //第三种情况为平年月份是2月28天的。
                                                 "(^\\d{3}[1-9]|\\d{2}[1-9]\\d{1}|\\d{1}[1-9]\\d{2}|[1-9]\\d{3}"
                                                 + //年
                                                 "([-/\\._]?)"
                                                 + //时间间隔符(-,/,.,_)
                                                 "(0?2)"
                                                 + //平年2月
                                                 "([-/\\._]?)"
                                                 + //时间间隔符(-,/,.,_)
                                                 "(2[0-8]|1[0-9]|0?[1-9])"
                                                 + //日(28)
                                                 "([\\s]?)"
                                                 + //空格
                                                 "((([0-1]?[0-9]|2[0-3]):([0-5]?[0-9]):([0-5]?[0-9]))?))$"
                                                 + //时分秒
                                                 "|"
                                                 + //或
                                                 //第四种情况为闰年月份是2月29天的。
                                                 //可以被4整除但不能被100整除的年份。
                                                 //可以被400整除的数亦是能被100整除，因此后两位是00，所以只要保证前两位能被4整除即可。
                                                 "(^((\\d{2})(0[48]|[2468][048]|[13579][26]))|((0[48]|[2468][048]|[13579][26])00)" + "([-/\\._]?)" + "(0?2)" + "([-/\\._]?)"
                                                 + "(29)" + "([\\s]?)" + "((([0-1]?\\d|2[0-3]):([0-5]?\\d):([0-5]?\\d))?))$" + //时分秒
                                                 ")";                                                                                                                                                                                                                                                               ;
}
