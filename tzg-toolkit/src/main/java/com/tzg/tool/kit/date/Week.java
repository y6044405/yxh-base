package com.tzg.tool.kit.date;

public enum Week {

    MONDAY(1, "星期一", "Monday", "Mon."), TUESDAY(2, "星期二", "Tuesday", "Tues."), WEDNESDAY(3, "星期三", "Wednesday", "Wed."), THURSDAY(4, "星期四", "Thursday", "Thur."), FRIDAY(5, "星期五",
                                                                                                                                                                         "Friday",
                                                                                                                                                                         "Fri."), SATURDAY(
                                                                                                                                                                                           6,
                                                                                                                                                                                           "星期六",
                                                                                                                                                                                           "Saturday",
                                                                                                                                                                                           "Sat."), SUNDAY(
                                                                                                                                                                                                           7,
                                                                                                                                                                                                           "星期日",
                                                                                                                                                                                                           "Sunday",
                                                                                                                                                                                                           "Sun.");

    int    id;
    String cn;
    String en;
    String enShort;

    Week(int id, String cn, String en, String enShort) {
        this.id = id;
        this.cn = cn;
        this.en = en;
        this.enShort = enShort;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCn() {
        return cn;
    }

    public void setCn(String cn) {
        this.cn = cn;
    }

    public String getEn() {
        return en;
    }

    public void setEn(String en) {
        this.en = en;
    }

    public String getEnShort() {
        return enShort;
    }

    public void setEnShort(String enShort) {
        this.enShort = enShort;
    }

}
